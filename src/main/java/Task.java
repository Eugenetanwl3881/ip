abstract class Task {
    protected String description;
    protected boolean isDone;


    public String getStatusIcon() {
        return (isDone ? "X" : " "); // mark done task with X
    }

    public void markDone() {
        this.isDone = true;
    }

    public void markUndone() {
        this.isDone = false;
    }

    abstract public String toString();
}
